CREATE TABLE USERS(
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    USERNAME VARCHAR2(50)
    UNIQUE NOT NULL,

    PASSWORD VARCHAR2(200)
    NOT NULL,

    ROLE VARCHAR2(20)
    NOT NULL,

    STATUS VARCHAR2(10)
    DEFAULT 'ACTIVE',

    CREATED_DATE DATE
    DEFAULT SYSDATE,

    CONSTRAINT CHK_ROLE
    CHECK (
        ROLE IN
        ('ADMIN','DISPATCHER','DRIVER')
    ),

    CONSTRAINT CHK_USER_STATUS
    CHECK (
        STATUS IN
        ('ACTIVE','INACTIVE')
    )
);

CREATE TABLE LOV (
    LOV_ID NUMBER(38)
        GENERATED BY DEFAULT AS IDENTITY
        PRIMARY KEY,

    LOV_TYPE VARCHAR2(100) NOT NULL,

    LOV_VALUE VARCHAR2(150) NOT NULL,

    STATUS VARCHAR2(10) NOT NULL,

    CONSTRAINT CHK_LOV_STATUS
        CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

